    private void armarCabecera1() {
        modelo.addColumn("IdMateria");
        modelo.addColumn("Nombre");
        modelo.addColumn("Año");
        jTinscripcion.setModel(modelo);
    }

    private void borrarFilas() {
        int f = jTinscripcion.getRowCount() - 1;
        for (; f >= 0; f--) {
            modelo.removeRow(f);
        }
    }

